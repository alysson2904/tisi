    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
         // TODO add your handling code here:
        int linhaClicada = jTable1.getSelectedRow();
        if(linhaClicada < 0 ){
            JOptionPane.showMessageDialog(null, "Primeiro você deve selecionar um cliente na tabela !!");
        }else{
              ProdutosDAO produtoDAO = new ProdutosDAO();
              ArrayList<Produtos> produtos = produtoDAO.consulta();
              preenche = produtos.get(linhaClicada);
              new AtualizaProduto().setVisible(true);
        }       
    }//GEN-LAST:event_jButton6ActionPerformed

