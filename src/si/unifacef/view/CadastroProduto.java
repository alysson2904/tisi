    private void jButtonInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirActionPerformed
        String no = jTextFieldNome.getText();
        String des = jTextAreaDescricao.getText();
        String pr = jTextFieldPreco.getText();
        //String set = jTextFieldSetor.getText();
        
        
        Produtos produto = new Produtos();
        produto.setNome(no);
        produto.setDescricao(des);
        produto.setPreco(pr);
        
        ProdutosDAO produtosDAO = new ProdutosDAO();
        int resultado = produtosDAO.insere(produto);
        if(resultado == 1){
            JOptionPane.showMessageDialog(null, "Registro inserido com sucesso!");
        }
        else{
            JOptionPane.showMessageDialog(null, "Problema na inserção");
        }
        
        jTextFieldNome.setText("");
        jTextAreaDescricao.setText("");
        jTextFieldPreco.setText("");
        
 
    
    }//GEN-LAST:event_jButtonInserirActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        dtm.setNumRows(0);
        ProdutosDAO produtosDAO = new ProdutosDAO();
            ArrayList<Produtos> produtos = produtosDAO.consulta();
            for(int i=0; i < produtos.size(); i++){
                String[] objeto = new String[6];
                objeto[0] = String.valueOf(produtos.get(i).getCodigo());
                objeto[1] = (produtos.get(i).getNome());
                objeto[2] = (produtos.get(i).getDescricao());
                objeto[3] = (produtos.get(i).getPreco());                
                dtm.addRow(objeto);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

